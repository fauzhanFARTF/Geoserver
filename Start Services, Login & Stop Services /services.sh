# Khusus Pengguna Mac
    # Start Services 
    di terminal Masuk ke dalam folder "/usr/local/geoserver/bin"
    dengan mengetikkan perintah di terminal
        "cd /usr/local/geoserver/bin"

    Jalankan services dengan mengetikkan perintah di terminal
        "sh startup.sh"

    untuk melihat isi dari folder "/usr/local/geoserver/bin" dengan mengetikkan perintah di terminal
        "ls"

    untuk menstop service cukup mengetikkan perintah
        "ctrl+c"